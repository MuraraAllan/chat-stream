class Graph {
  constructor() {
    this.graph = {};
  }

  addNode(newNode, toNode = null) {
    if (!this.graph[newNode]) {
      this.graph[newNode] = new Set();
    }
    if (toNode) {
      this.addEdge(newNode, toNode);
    }
  }

  addEdge(fromNode, toNode) {
    if (!this.graph[fromNode]) this.addNode(fromNode);
    if (!this.graph[toNode]) this.addNode(toNode);
    this.graph[fromNode].add(toNode);
    this.graph[toNode].add(fromNode);
  }

  mergeGraph(otherGraph) {
    for (const [node, edges] of Object.entries(otherGraph)) {
      if (!this.graph[node]) {
        this.addNode(node);
      }
      for (const edge of edges) {
        this.addEdge(node, edge);
      }
    }
  }

  toJSON() {
    const result = {};
    for (const [node, edges] of Object.entries(this.graph)) {
      result[node] = Array.from(edges);
    }
    return result;
  }
}

function mergeGraphs(graph1, graph2) {
  const mergedGraph = { ...graph1 };

  for (const [node, edges] of Object.entries(graph2)) {
    if (!mergedGraph[node]) {
      mergedGraph[node] = [...edges];
    } else {
      for (const edge of edges) {
        if (!mergedGraph[node].includes(edge)) {
          mergedGraph[node].push(edge);
        }
        // Ensure bidirectional relationship
        if (!mergedGraph[edge]) {
          mergedGraph[edge] = [node];
        } else if (!mergedGraph[edge].includes(node)) {
          mergedGraph[edge].push(node);
        }
      }
    }
  }

  return mergedGraph;
}

